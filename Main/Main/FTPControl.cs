        public delegate void DoProcess();
        public event DoProcess ProcessDone;

        public void Done()
        {
            if (ProcessDone != null)
            {
                ProcessDone();
                EmptyDelegate();
            }
        }
        private void EmptyDelegate()
        {
            ProcessDone = null;
        }
    }
